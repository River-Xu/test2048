class Cell{
    constructor(x,y,value){
        this.x = x;
        this.y = y;
        this.value = value;
    }
    getX(){
        return this.x;
    }
    setX(x){
        this.x = x;
    }
    getY(){
        return this.y;
    }
    setY(y){
        this.y = y;
    }
    getValue(){
        return this.value;
    }
    setValue(value){
        this.value = value;
    }
}